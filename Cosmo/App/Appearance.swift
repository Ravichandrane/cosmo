import Foundation

enum Appearance {
    enum FontSize {
        /// FontSize of  32
        static let big: CGFloat = 34
        /// FontSize of  24
        static let medium: CGFloat = 24
        /// FontSize of  16
        static let normal: CGFloat = 16
        /// FontSize of  14
        static let small: CGFloat = 14
        /// FontSize of  12
        static let extraSmall: CGFloat = 12
    }
    
    enum Padding {
        /// Padding of  24
        static let medium: CGFloat = 24
        /// Padding of  16
        static let normal: CGFloat = 16
        /// Padding of  8
        static let small: CGFloat = 8
        /// Padding of  4
        static let extraSmall: CGFloat = 4
    }
}
